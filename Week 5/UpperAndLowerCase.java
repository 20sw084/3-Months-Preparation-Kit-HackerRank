/*
Write a java program to convert string into
both upper and lowe cases and print them
 */
package com.java;
public class UpperAndLowerCase {
    public static void main(String[] args) {
        String s1="Computer";
        System.out.println(s1);
        String s2=s1.toLowerCase();
        System.out.println(s2);
        s2=s1.toUpperCase();
        System.out.println(s2);
    }
}
